package Sort;

public class 时间复杂度 {
   /**
    * 时间复杂度，算法的时间量度
    * T(n) = O(f(n))
    * O(1)<O(logn)<O(n)<O(nlogn)<O(n^2)<O(n^3)<O(2^n)<O(n!)<O(n^n)
    */

   /**
    * 根据排序过程中的记录是否全部被放置在内存中，排序可分为内排序和外排序
    * 内排序：带排的所有记录全部被放在内存中
    * 外排序：排序的记录太多，不能同时放到内存
    *
    */

   /**
    * 排序算法性能因素
    * 1.时间性能：
    * 比较与移动，关键字直接比较，记录从一个位置移动到另一个位置
    * 2.辅助空间：
    * 除了存储待排序所占用的存储空间外，执行算法所需要的其他空间
    * 3.算法的复杂性：
    * 算法的复杂程度
    * 内排序：插入排序、交换排序、选择排序、归并排序。
    *
    */
}
